    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
            int checkUserExist =0;
     String id = medid.getText();
     if(id.equals("")){
         JOptionPane.showMessageDialog(null, "Medicine ID field is required.");
     }else {
         try{
             
             ConnectionProvider connectionProvider = new ConnectionProvider();
            Connection con = connectionProvider.getCon();
            Statement st = con.createStatement();
            ResultSet rs = st.executeQuery("select * from stock where Id = '" + id + "'");
            while (rs.next()){
                medid.setEditable(false);
                checkUserExist = 1;
                txtName.setText(rs.getString("medicineName"));
                txtsupl.setText(rs.getString("supplierName"));
                txtdom.setText(rs.getString("dom"));
                txtdoe.setText(rs.getString("doe"));
                txtdate.setText(rs.getString("purchasedate"));
                txtprice.setText(rs.getString("price"));
                txtqty.setText(rs.getString("quantity"));
             
            }
         }
         catch(Exception e){
            JOptionPane.showMessageDialog(null, e);  
         }
         if(checkUserExist == 0){
               JOptionPane.showMessageDialog(null,"Medicine ID does not exist.");
         }
     }
    }//GEN-LAST:event_jButton3ActionPerformed

