    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
   
    String id = medid.getText();
    String name = txtName.getText();
    String sname = txtsupl.getText();
    String dom = txtdom.getText();
    String doe = txtdoe.getText();
    String price = txtprice.getText();
    String date = txtdate.getText();
    String qty = txtqty.getText();
     SimpleDateFormat dateFormat = new SimpleDateFormat("dd/MM/yyyy");
       int qnty = Integer.parseInt(qty);
     double prc = Double.parseDouble(price);
     double Price=2*prc;
     double total = qnty*Price;
     String Total = Double.toString(total);
     String priceper = Double.toString(Price);
    try {
         dateFormat.setLenient(false);
        Date parsedDom = dateFormat.parse(dom);
        String formattedDom = dateFormat.format(parsedDom);
        Date parsedDoe = dateFormat.parse(doe);
        String formattedDoe = dateFormat.format(parsedDoe);
        
        ConnectionProvider connectionProvider = new ConnectionProvider();
        Connection con = connectionProvider.getCon();

        PreparedStatement updateStock = con.prepareStatement("UPDATE stock SET medicineName=?, supplierName=?, dom=?, doe=?, price=?, purchasedate=?, quantity=? WHERE Id=?");
        updateStock.setString(1, name);
        updateStock.setString(2, sname);
        updateStock.setString(3, dom);
        updateStock.setString(4, doe);
        updateStock.setString(5, price);
        updateStock.setString(6, date);
        updateStock.setString(7, qty);
        updateStock.setString(8, id);

        int stockUpdateCount = updateStock.executeUpdate();

        if (stockUpdateCount > 0) {
       
            PreparedStatement updateInventory = con.prepareStatement("UPDATE inventory SET medicineName=?, dom=?, doe=?, quantity=?, price=?, totalPrice=? WHERE Id=?");
            updateInventory.setString(1, name);
            updateInventory.setString(2, dom);
            updateInventory.setString(3, doe);
            updateInventory.setString(4, qty);
            updateInventory.setString(5, priceper);
            updateInventory.setDouble(6, total);
            updateInventory.setString(7, id);

            int inventoryUpdateCount = updateInventory.executeUpdate();

            if (inventoryUpdateCount > 0) {
                JOptionPane.showMessageDialog(null, "Stock and Inventory updated successfully.");
            } else {
                JOptionPane.showMessageDialog(null, "Stock updated, but Inventory update failed.");
            }
        } else {
            JOptionPane.showMessageDialog(null, "No rows updated. Stock ID not found.");
        }

        setVisible(false);
        new UpdateStock().setVisible(true);
    } catch (Exception e) {
        JOptionPane.showMessageDialog(null, "Date Field is invalid. Use the format 'dd/MM/yyyy'.");
    }

 
    }//GEN-LAST:event_jButton1ActionPerformed

