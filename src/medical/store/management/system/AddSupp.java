    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
    String Id = txtsuppid.getText();
    String supplierName = txtsuppname.getText();
    String mobileNumber = txtmobile.getText();
    String address = txtaddress.getText();
   
    if(Id.equals("")){
        JOptionPane.showMessageDialog(null, "Supplier ID field is required.");
    }else if(checkSupplier_ID == 1){
       JOptionPane.showMessageDialog(null, "This supplier ID already exist."); 
     }else if(supplierName.equals("")){
        JOptionPane.showMessageDialog(null, "Supplier Name field is required.");
    }else if(mobileNumber.equals("")){
        JOptionPane.showMessageDialog(null, "Mobile number field is required.");
     }else if(!mobileNumber.matches(MobileNumberPattern) || mobileNumber.length()!=10){
        JOptionPane.showMessageDialog(null, "Mobile Number field is invalid.");
    }else if(address.equals("")){
        JOptionPane.showMessageDialog(null, "Address field is required.");
    }else {
        try{
             ConnectionProvider connectionProvider = new ConnectionProvider();
            Connection con = connectionProvider.getCon();
            PreparedStatement ps = con.prepareStatement("insert into supplier(Id,supplierName,mobileNumber,address) values(?,?,?,?)");
            ps.setString(1, Id);
            ps.setString(2, supplierName);
            ps.setString(3, mobileNumber);
            ps.setString(4, address);
            ps.executeUpdate();
            JOptionPane.showMessageDialog(null, "Supplier Added Successfully.");
            setVisible(false);
            new AddSupp().setVisible(true);
        }
        catch(Exception e){
        JOptionPane.showMessageDialog(null, e);
       }
    }
    }//GEN-LAST:event_jButton1ActionPerformed

