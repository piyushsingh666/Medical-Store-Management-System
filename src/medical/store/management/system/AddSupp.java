    private void txtsuppidKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtsuppidKeyReleased
        // TODO add your handling code here:
         String Id = txtsuppid.getText();
    if (!Id.equals("")) {
        txtLabel.setVisible(true);
        txtLabel.setText("");
        checkSupplier_ID = 0;
        
        try {
            ConnectionProvider connectionProvider = new ConnectionProvider();
            Connection con = connectionProvider.getCon();
            Statement st = con.createStatement();
            ResultSet rs = st.executeQuery("select * from supplier where Id = '" + Id + "'");
            while (rs.next()) {
                checkSupplier_ID = 1; 
            }
           
            if (checkSupplier_ID == 1) {
                txtLabel.setText("This supplier ID already exists");
            } else {
                txtLabel.setText("");
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }
    } else {
        txtLabel.setVisible(false);
    }
    }//GEN-LAST:event_txtsuppidKeyReleased

